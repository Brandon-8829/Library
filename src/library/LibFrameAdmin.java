    private void fillAdminTable(){
       DefaultTableModel model = (DefaultTableModel) libTable.getModel();
       model.setRowCount(0);
       for(int i = 0; i < BookData.size; i++) {
        model.addRow(new Object[] { i,
                BookData.inventory[i].getTitle(), 
                BookData.inventory[i].getAuthor(),
                BookData.inventory[i].getIsbn(), 
                BookData.inventory[i].getGenre(), 
                BookData.inventory[i].isCheckedIn() });
    }
    }
    
    private void fillUserTable(){
       DefaultTableModel model = (DefaultTableModel) userTable.getModel();
       model.setRowCount(0);
       for(int i = 0; i < UserData.userSize; i++) {
       model.addRow(new Object [] {
       
           UserData.logins[i].getUser(),
           UserData.logins[i].getFees(),
           UserData.logins[i].getId(),
           UserData.logins[i].isReserved(),
                   });
       }
    }
    
